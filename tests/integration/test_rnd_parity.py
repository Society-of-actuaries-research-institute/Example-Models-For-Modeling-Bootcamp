@pytest.mark.parametrize("scenario_idx", range(25))
def test_pv_matches_rnd_within_01(production_pv: np.ndarray, scenario_idx: int) -> None:
    """Production PV must be within $0.01 of the RnD scalar calculation."""
    rnd_pv = _rnd_pv_for_scenario(scenario_idx + 1)
    prod_pv = float(production_pv[scenario_idx])
    diff = abs(prod_pv - rnd_pv)
