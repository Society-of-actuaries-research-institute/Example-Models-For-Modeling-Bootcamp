def _rnd_calculate_policy(policy_number: int, scenario_number: int) -> list[dict]:
    """Direct port of RnD_Model_example.calculate_policy — used as ground truth."""
    policy = _POLICIES[policy_number - 1]
    random_number = _RANDOM_NUMBERS[scenario_number - 1][policy_number - 1]
    if policy.gender == "M":
        base_qx_table = _MALE_QX.tolist()
        improvement_table = _MALE_IMP.tolist()
    else:
        base_qx_table = _FEMALE_QX.tolist()
        improvement_table = _FEMALE_IMP.tolist()

    results = []
    tpx = 1.0
    for projection_year in range(2025, 2086):
        age = projection_year - policy.yob
        if age >= len(base_qx_table):
            base_qx = 1.0
            improvement = 1.0
        else:
            base_qx = base_qx_table[age]
            improvement = (1 - improvement_table[age]) ** (projection_year - 2012)
        improved_qx = base_qx * improvement
        px = 1 - improved_qx
        tpx *= px
        cum_death = 1 - tpx
        survive = int(random_number > cum_death)
        results.append({"survive": survive, "cf": policy.annual_benefit * survive})
    return results


def _rnd_pv_for_scenario(scenario_number: int) -> float:
    """Direct port of RnD_Model_example.calculate_pv_cash_flows_by_scenario."""
    valuation_year = 2024
    pv = 0.0
    for year_idx in range(61):  # 2025..2085
        year = 2025 + year_idx
        total_cf = sum(
            _rnd_calculate_policy(p, scenario_number)[year_idx]["cf"] for p in range(1, 11)
        )
        pv += total_cf / (1 + _DISCOUNT_RATE) ** (year - valuation_year)
    return pv


